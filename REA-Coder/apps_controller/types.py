from dataclasses import dataclass, field
from typing import List, Dict, Optional


@dataclass
class IterationState:
    # full question list used in current iteration
    question_template: List[Dict[str, str]] = field(default_factory=list)

    # previous round incorrect question ids (for next-round base)
    last_incorrect_qids: List[str] = field(default_factory=list)

    # evolving enhanced requirement text (becomes next-round base_requirement if tests fail)
    requirement_text: str = ""

    pending_mask_gap_blocks_text: str = ""

    # NEW: dynamic stop testcases generated by Agent2 (after round-1)
    dynamic_stop_test_cases: Dict[str, List[str]] = field(default_factory=lambda: {"inputs": [], "outputs": []})

    # NEW: fail streak for model-generated stop cases (keyed by a stable signature)
    dynamic_stop_streak: Dict[str, int] = field(default_factory=dict)
    pending_masked_text: str = ""